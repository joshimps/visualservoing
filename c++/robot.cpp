#include "robot.h"


///////////////////////////////////////////////////////////////////////////////////////////
// Constructors and Destructors
/////////////////////////////////////////////////////////////////////////////////////////

Robot::Robot(ros::NodeHandle nh, std::vector<double> d, std::vector<double> a, std::vector<double> alpha){
    nh_ = nh;
    jointStateSub_ = nh_.subscribe("/joint_states", 1000, &Robot::jointStateCallBack, this);
}

 ///////////////////////////////////////////////////////////
// Callbacks
//////////////////////////////////////////////////////////
void Robot::jointStateCallBack(sensor_msgs::JointState &msg){
    jointStates_ = *msg;
}

///////////////////////////////////////////////////////////////////////////////////////////
// Getters
/////////////////////////////////////////////////////////////////////////////////////////

sensor_msgs::JointState Robot::getJointState(){
    return jointStates_;
}